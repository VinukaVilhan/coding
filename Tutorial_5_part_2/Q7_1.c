#include<stdio.h>
void main()
{

    int a; 
    a=2;
    printf("a = %d\n",a); 
    printf("a++ = %d\n",a++); 
    printf("a = %d\n",a); 
    printf("a-- = %d\n",a--); 
    printf("a = %d\n",a); 
    printf("++a = %d\n",++a); 
    printf("a = %d\n",a); 
    printf("--a = %d\n",--a);
    printf("a = %d\n", a);

}
